#Exercicio 3; valor do volume do cilindro pelo raio e a altura
raio = float(input('informe o valor do raio do cilindro em metros:'))
altura = float(input('informe a altura do cilindro em metros:'))
volume_cilindro = 3.1416 * raio * raio * altura
print('O volume do clindro:', volume_cilindro, 'mÂ³' )
